/**
 * Return `count` pre-built fallback questions for the given grade.
 * Always returns a non-empty array (falls back to grade 1 if grade unknown).
 * Questions are shuffled to avoid always showing the same set.
 */
export function getFallbackQuestions(grade: number, count: number): AutoQuestion[] {
  const bank = BANKS[grade] ?? BANKS[1];
  const shuffled = [...bank].sort(() => Math.random() - 0.5);
  const result: AutoQuestion[] = [];
  let i = 0;
  while (result.length < count) {
    result.push({ ...shuffled[i % shuffled.length], id: `auto-${randomUUID()}` });
    i++;
  }
  return result.slice(0, count);
}
